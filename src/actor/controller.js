import { ObjectId } from 'mongodb';
import client from '../common/dbconn.js';
import { Actor } from './actor.js';  // Asumimos que tienes un esquema para Actor

const actorCollection = client.db('vivero').collection('actores');  // Colección de actores
const peliculaCollection = client.db('vivero').collection('peliculas');  // Colección de películas

async function handleInsertActorRequest(req, res) {
    let data = req.body;
    let actor = new Actor();
    
    // Asignamos los valores al objeto actor
    actor.nombre = data.nombre;
    actor.edad = data.edad;
    actor.genero = data.genero;
    
    // Validar si la película existe
    try {
        let pelicula = await peliculaCollection.findOne({ titulo: data.idPelicula });
        if (!pelicula) {
            return res.status(400).send('Película no encontrada con el nombre proporcionado');
        }

        actor.idPelicula = pelicula._id;  // Asignamos el _id de la película al actor

        // Insertamos el actor en la colección
        await actorCollection.insertOne(actor)
            .then((data) => {
                if (data === null) {
                    return res.status(400).send('Error al guardar actor');
                }
                return res.status(201).send(data);
            })
            .catch((e) => {
                return res.status(500).send({ error: e });
            });

    } catch (e) {
        return res.status(500).send({ error: 'Error al buscar la película', details: e });
    }
}

async function handleGetActoresRequest(req, res) {
    await actorCollection.find({}).toArray()
        .then((data) => {
            return res.status(200).send(data);
        })
        .catch((e) => {
            return res.status(500).send({ error: e });
        });
}

async function handleGetActorByIdRequest(req, res) {
    let id = req.params.id;
    try {
        let oid = ObjectId.createFromHexString(id);
        await actorCollection.findOne({ _id: oid })
            .then((data) => {
                if (data === null) return res.status(404).send('Actor no encontrado');
                return res.status(200).send(data);
            })
            .catch((e) => {
                return res.status(500).send({ error: e });
            });
    } catch (e) {
        return res.status(400).send('ID mal formado');
    }
}

async function handleGetActoresByPeliculaIdRequest(req, res) {
    let idPelicula = req.params.pelicula;
    try {
        let oid = ObjectId.createFromHexString(idPelicula);
        await actorCollection.find({ idPelicula: oid }).toArray()
            .then((data) => {
                return res.status(200).send(data);
            })
            .catch((e) => {
                return res.status(500).send({ error: e });
            });
    } catch (e) {
        return res.status(400).send('ID de película mal formado');
    }
}

export default {
    handleInsertActorRequest,
    handleGetActoresRequest,
    handleGetActorByIdRequest,
    handleGetActoresByPeliculaIdRequest
};










































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































